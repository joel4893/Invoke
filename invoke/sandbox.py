        def utc_ms():
            return int(time.time() * 1000)


        def append_jsonl(path, payload):
            path.parent.mkdir(parents=True, exist_ok=True)
            with path.open("a", encoding="utf-8") as fh:
                fh.write(json.dumps(payload, sort_keys=True) + "\\n")


        def read_jsonl(path):
            if not path.exists():
                return []
            rows = []
            for line in path.read_text(encoding="utf-8").splitlines():
                try:
                    rows.append(json.loads(line))
                except json.JSONDecodeError:
                    continue
            return rows


        @dataclass
        class AgentTrace:
            trace_id: str
            agent_id: str
            agent_name: str
            timestamp: datetime
            status: str
            execution_id: str | None = None
            action: str | None = None
            stage: str | None = None
            data: dict[str, Any] = field(default_factory=dict)

            def model_dump(self):
                payload = asdict(self)
                payload["timestamp"] = self.timestamp.isoformat()
                return payload

            def model_dump_json(self):
                return json.dumps(self.model_dump(), sort_keys=True)


        def trace_db_path():
            return Path("/state/traces.db")


        def ensure_trace_db():
            path = trace_db_path()
            path.parent.mkdir(parents=True, exist_ok=True)
            conn = sqlite3.connect(path)
            conn.execute(
                '''
                CREATE TABLE IF NOT EXISTS traces (
                    trace_id TEXT PRIMARY KEY,
                    agent_id TEXT NOT NULL,
                    agent_name TEXT NOT NULL,
                    timestamp TEXT NOT NULL,
                    status TEXT NOT NULL,
                    execution_id TEXT,
                    action TEXT,
                    stage TEXT,
                    data TEXT NOT NULL
                )
                '''
            )
            return conn


        async def store_trace(trace: AgentTrace):
            payload = trace.model_dump()
            conn = ensure_trace_db()
            try:
                conn.execute(
                    '''
                    INSERT OR REPLACE INTO traces
                    (trace_id, agent_id, agent_name, timestamp, status, execution_id, action, stage, data)
                    VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)
                    ''',
                    (
                        trace.trace_id,
                        trace.agent_id,
                        trace.agent_name,
                        payload["timestamp"],
                        trace.status,
                        trace.execution_id,
                        trace.action,
                        trace.stage,
                        trace.model_dump_json(),
                    ),
                )
                conn.commit()
            finally:
                conn.close()
            append_jsonl(Path("/state/agent_traces.jsonl"), payload)


        async def record_trace(data: dict):
            trace = AgentTrace(
                trace_id=data.get("trace_id") or data.get("execution_id") or "trace_" + uuid.uuid4().hex[:12],
                agent_id=data.get("agent_id") or APP_NAME,
                agent_name=data.get("agent_name") or APP_NAME,
                timestamp=datetime.utcnow(),
                status=data.get("status") or "event",
                execution_id=data.get("execution_id"),
                action=data.get("action"),
                stage=data.get("stage"),
                data=data,
            )
            await store_trace(trace)


        def onyx_summarize(traces):
            recent = traces[-50:]
            groups = {{}}
            for trace in recent:
                action = str(trace.get("action") or "").lower()
                outcome = str(trace.get("final_outcome") or trace.get("status") or "").lower()
                events = trace.get("events") if isinstance(trace.get("events"), list) else []
                event_text = " ".join(str(event.get("step") or "") + " " + str(event.get("status") or "") for event in events if isinstance(event, dict)).lower()
                key = None
                if "timeout" in outcome or "timeout" in event_text:
                    key = "timeout_unknown_effect"
                elif "blocked_by_policy" in outcome or action == "database.execute":
                    key = "policy_block"
                elif "duplicate" in outcome or "idempotent" in event_text:
                    key = "duplicate_retry"
                elif "state_reconciled changed" in event_text or "state_revalidated changed" in event_text:
                    key = "state_drift"
                if key:
                    groups[key] = groups.get(key, 0) + 1

            suggestions = []
            if groups.get("timeout_unknown_effect", 0) >= 2:
                suggestions.append({{
                    "title": "Require reconciliation before retry",
                    "severity": "high",
                    "confidence": 90,
                    "reason": f"Seen {{groups['timeout_unknown_effect']}} unknown-effect timeout traces.",
                    "one_click_patch": {{"retry_strategy": "reconcile_before_retry"}},
                }})
            if groups.get("duplicate_retry", 0) >= 2:
                suggestions.append({{
                    "title": "Require idempotency keys",
                    "severity": "high",
                    "confidence": 88,
                    "reason": f"Seen {{groups['duplicate_retry']}} duplicate retry traces.",
                    "one_click_patch": {{"idempotency": {{"mode": "required"}}}},
                }})
            if groups.get("policy_block", 0) >= 1:
                suggestions.append({{
                    "title": "Keep unsafe actions in preflight",
                    "severity": "high",
                    "confidence": 86,
                    "reason": f"Seen {{groups['policy_block']}} policy-blocked unsafe actions.",
                    "one_click_patch": {{"preflight": {{"block": ["database.execute", "*.delete"]}}}},
                }})
            if groups.get("state_drift", 0) >= 1:
                suggestions.append({{
                    "title": "Revalidate state before resume",
                    "severity": "high",
                    "confidence": 87,
                    "reason": f"Seen {{groups['state_drift']}} state drift traces.",
                    "one_click_patch": {{"freeze_thaw": {{"revalidate_before_resume": True}}}},
                }})
            return {{
                "enabled": True,
                "mode": "deterministic",
                "status": "watching",
                "trace_count": len(recent),
                "suggestions": suggestions[:3],
            }}


        def evaluate_policy(action, params):
            lowered = str(action).lower()
            sql = str(params.get("sql", "")).lower() if isinstance(params, dict) else ""
            if action == "database.execute" or "disable row level security" in sql or "drop table" in sql:
                return {{
                    "effect": "block",
                    "risk": "high",
                    "reason": "Direct database execution can bypass application policy or row-level security.",
                    "guardrails": ["policy_block", "sandbox_required"],
                }}
            if any(word in lowered for word in ("delete", "refund", "charge", "transfer")):
                return {{
                    "effect": "require_approval",
                    "risk": "high",
                    "reason": "Financial or destructive action requires approval and reconciliation.",
                    "guardrails": ["approval", "idempotency_key", "state_reconciliation"],
                }}
            return {{
                "effect": "allow",
                "risk": "low",
                "reason": "No blocking policy matched.",
                "guardrails": ["trace"],
            }}


        def reconcile_state(expected, live):
            drift = []
            if isinstance(expected, dict) and isinstance(live, dict):
                for key, expected_value in expected.items():
                    if live.get(key) != expected_value:
                        drift.append({{"key": key, "expected": expected_value, "live": live.get(key)}})
            return {{"status": "changed" if drift else "valid", "drift": drift, "checked_at_ms": utc_ms()}}


        def extract_action(payload):
            raw = payload.get("action") or {{}}
            if isinstance(raw, str):
                return raw, payload.get("params") or {{}}
            if isinstance(raw, dict):
                return raw.get("action") or raw.get("name") or "agent.run", raw.get("params") or {{}}
            return "agent.run", {{}}


        def checkpoint_path():
            return Path("/state/checkpoints.jsonl")


        def trace_path():
            return Path("/state/traces.jsonl")


        def freeze_execution(execution_id, action, params, context):
            checkpoint = {{
                "checkpoint_id": "freeze_" + uuid.uuid4().hex[:12],
                "execution_id": execution_id,
                "action": action,
                "params": params,
                "context_snapshot": context,
                "created_at_ms": utc_ms(),
            }}
            append_jsonl(checkpoint_path(), checkpoint)
            return checkpoint


        def find_checkpoint(checkpoint_id):
            for checkpoint in reversed(read_jsonl(checkpoint_path())):
                if checkpoint.get("checkpoint_id") == checkpoint_id:
                    return checkpoint
            return None


        __AGENT_COMMAND_SOURCE__


        @app.function(image=image, volumes={{"/state": volume}}, secrets=function_secrets, timeout=60 * 20)
        @modal.fastapi_endpoint(method="POST", label={endpoint_name})
        async def invoke(request: Request):
            started = utc_ms()
            payload = await request.json()
            execution_id = payload.get("execution_id") or "exec_" + uuid.uuid4().hex[:12]
            prompt = payload.get("prompt") or payload.get("input") or ""
            agent_id = payload.get("agent_id") or APP_NAME
            action, params = extract_action(payload)
