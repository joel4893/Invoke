    volume_name = json.dumps(plan.modal_volume)
    project_root = json.dumps(plan.project_root)
    invoke_package_root = json.dumps(str(Path(__file__).parent.resolve()))
    runtime_root = json.dumps(str(Path(plan.project_root) / ".invoke_runtime"))
    env_keys = json.dumps(sorted(plan.env), sort_keys=True)
    robustness = repr(dict(sorted(plan.robustness.items())))
    endpoint_name = json.dumps(plan.endpoint_name)
